package indwins.c3.lift.source;

public class Rating 
{
	public String ratedByUser;
	public String ratedUser;
	public String rating;
	public String comment;
	public String getRatedByUser() 
	{
		return ratedByUser;
	}
	public void setRatedByUser(String ratedByUser) 
	{
		this.ratedByUser = ratedByUser;
	}
	public String getRatedUser() 
	{
		return ratedUser;
	}
	public void setRatedUser(String ratedUser) 
	{
		this.ratedUser = ratedUser;
	}
	public String getRating() 
	{
		return rating;
	}
	public void setRating(String rating) 
	{
		this.rating = rating;
	}
	public String getComment() 
	{
		return comment;
	}
	public void setComment(String comment) 
	{
		this.comment = comment;
	}
}
